package fmcr.util;
/**
 * Tags a Report object. 
 * A FIELD tag indicates that the Report contains information about a visited field
 * A METHOD tag indicates that the Report contains information about a visited method 
 *
 * @author  Inah Omoronyia
 * @version 1.0
 * @see     fmcr.utilReport
 */
public enum ReportTag {
    FIELD, METHOD
}

